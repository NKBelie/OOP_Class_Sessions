public class ComparisonOperators {
    public static void main(String[] args) {
        int num1=299;
        int num2=32;
        if (num1 > num2){
            System.out.println("Num1 is greater to Num2");
        } else if (num1 == num2) {
            System.out.println("Num1 is equal to Num2");
        }
        else{
            System.out.println("Num1 is less than Num2");
        }
    }
}
